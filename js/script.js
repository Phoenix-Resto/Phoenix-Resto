function openPdf()
{
var omyFrame = document.getElementById("myFrame");
omyFrame.style.display="block";
omyFrame.src = "https://docs.google.com/viewer?url=https://raw.githubusercontent.com/phoenix-resto/Phoenix-Resto/images/phoenix-menu.pdf&embedded=true";
omyFrame.scrollIntoView({ behavior: 'smooth' });
}

