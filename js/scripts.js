function openPdf()
{
var omyFrame = document.getElementById("myFrame");
omyFrame.style.display="block";
omyFrame.src = "http://docs.google.com/viewer?url=https://github.com/Akhil-1573/Hello-Akhi/raw/main/phoenix-menu.pdf&embedded=true";
omyFrame.scrollIntoView({ behavior: 'smooth' });
}

